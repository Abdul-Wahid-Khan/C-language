// do while loop
#include <stdio.h>

int main() {
    //Use of increment i++    
    int i=0;
    do{
        printf("%d\n", i);
        i++;
    }while (i<=10);
    
    //Use of decrement j--
    int j=5;
    do{
        printf("%d\n", j);
        j--;
    }while (j>=0);

    return 0;

}

